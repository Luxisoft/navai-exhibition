const DEFAULT_BUYER_NAMES = new Set<string>(["Demo Buyer", ...Object.values(BUYER_NAME_MAP)]);

function languageMatches(language: string, base: string) {
  const normalized = String(language || "").toLowerCase();
  return normalized === base || normalized.startsWith(`${base}-`) || normalized.startsWith(`${base}_`);
}

function resolveDemoLanguage(language: string): DemoLanguage | null {
  for (const candidate of DEMO_LANGUAGES) {
    if (languageMatches(language, candidate)) return candidate;
  }
  return null;
}

export function getEcommerceStoreDemoText(language: string, englishText: string) {
  const demoLanguage = resolveDemoLanguage(language);
  if (!demoLanguage) return englishText;
  return UI_TEXT_MAPS[demoLanguage][englishText] ?? englishText;
}

export function getEcommerceStoreDemoRuntimeMessage(language: string, message: string) {
  const demoLanguage = resolveDemoLanguage(language);
  if (!demoLanguage) return message;
  return RUNTIME_MESSAGE_MAPS[demoLanguage][message] ?? message;
}

export function getEcommerceStoreDemoStatusLabel(language: string, value: string) {
  const key = value.trim().toLowerCase();
  const demoLanguage = resolveDemoLanguage(language);
  if (!demoLanguage) return value;
  return STATUS_LABEL_MAPS[demoLanguage][key] ?? value;
}

export function getEcommerceStoreDemoBuyerName(language: string) {
  const demoLanguage = resolveDemoLanguage(language);
  if (!demoLanguage) return "Demo Buyer";
  return BUYER_NAME_MAP[demoLanguage] ?? "Demo Buyer";
}

export function isEcommerceStoreDemoDefaultBuyerName(value: string) {
  return DEFAULT_BUYER_NAMES.has(String(value || "").trim());
}
