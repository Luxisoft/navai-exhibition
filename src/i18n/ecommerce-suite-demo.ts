type EcommerceSuiteI18nLanguage = "es" | "fr" | "pt" | "zh" | "ja" | "ru" | "ko" | "hi";

const ECOMMERCE_SUITE_I18N_MAPS: Record<EcommerceSuiteI18nLanguage, Record<string, string>> = {
  es: ES_ECOMMERCE_SUITE_TEXT_MAP,
  fr: FR_ECOMMERCE_SUITE_TEXT_MAP,
  pt: PT_ECOMMERCE_SUITE_TEXT_MAP,
  zh: ZH_ECOMMERCE_SUITE_TEXT_MAP,
  ja: JA_ECOMMERCE_SUITE_TEXT_MAP,
  ru: RU_ECOMMERCE_SUITE_TEXT_MAP,
  ko: KO_ECOMMERCE_SUITE_TEXT_MAP,
  hi: HI_ECOMMERCE_SUITE_TEXT_MAP,
};

function languageMatches(language: string, base: EcommerceSuiteI18nLanguage) {
  const normalized = String(language || "").toLowerCase();
  return normalized === base || normalized.startsWith(`${base}-`) || normalized.startsWith(`${base}_`);
}

function resolveLanguage(language: string): EcommerceSuiteI18nLanguage | null {
  if (languageMatches(language, "es")) return "es";
  if (languageMatches(language, "fr")) return "fr";
  if (languageMatches(language, "pt")) return "pt";
  if (languageMatches(language, "zh")) return "zh";
  if (languageMatches(language, "ja")) return "ja";
  if (languageMatches(language, "ru")) return "ru";
  if (languageMatches(language, "ko")) return "ko";
  if (languageMatches(language, "hi")) return "hi";
  return null;
}

export function getEcommerceSuiteDemoText(language: string, englishText: string) {
  const key = String(englishText ?? "");
  const resolved = resolveLanguage(language);
  if (!resolved) return key;
  return ECOMMERCE_SUITE_I18N_MAPS[resolved][key] ?? getEcommerceSuiteDemoFieldLabelText(language, key) ?? key;
}
