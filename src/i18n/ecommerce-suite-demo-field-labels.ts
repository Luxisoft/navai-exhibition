const ECOMMERCE_SUITE_FIELD_LABEL_MAPS: Record<EcommerceSuiteFieldLabelLanguage, Record<string, string>> = {
  es: ES_ECOMMERCE_SUITE_FIELD_LABELS,
  fr: FR_ECOMMERCE_SUITE_FIELD_LABELS,
  pt: PT_ECOMMERCE_SUITE_FIELD_LABELS,
  zh: ZH_ECOMMERCE_SUITE_FIELD_LABELS,
  ja: JA_ECOMMERCE_SUITE_FIELD_LABELS,
  ru: RU_ECOMMERCE_SUITE_FIELD_LABELS,
  ko: KO_ECOMMERCE_SUITE_FIELD_LABELS,
  hi: HI_ECOMMERCE_SUITE_FIELD_LABELS,
};

function languageMatches(language: string, base: EcommerceSuiteFieldLabelLanguage) {
  const normalized = String(language || "").toLowerCase();
  return normalized === base || normalized.startsWith(`${base}-`) || normalized.startsWith(`${base}_`);
}

function resolveLanguage(language: string): EcommerceSuiteFieldLabelLanguage | null {
  if (languageMatches(language, "es")) return "es";
  if (languageMatches(language, "fr")) return "fr";
  if (languageMatches(language, "pt")) return "pt";
  if (languageMatches(language, "zh")) return "zh";
  if (languageMatches(language, "ja")) return "ja";
  if (languageMatches(language, "ru")) return "ru";
  if (languageMatches(language, "ko")) return "ko";
  if (languageMatches(language, "hi")) return "hi";
  return null;
}

export function getEcommerceSuiteDemoFieldLabelText(language: string, englishText: string) {
  const resolved = resolveLanguage(language);
  if (!resolved) return null;
  return ECOMMERCE_SUITE_FIELD_LABEL_MAPS[resolved][String(englishText ?? "")] ?? null;
}
