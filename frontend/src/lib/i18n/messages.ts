const DEFAULT_MESSAGES = es as AppMessages;

const APP_MESSAGES_CACHE: Partial<Record<LanguageCode, AppMessages>> = {
  es: DEFAULT_MESSAGES,
};

const APP_MESSAGES_LOADERS: Record<
  Exclude<LanguageCode, "es">,
  () => Promise<AppMessages>
> = {
  en: async () => (await import("@/i18n/en.json")).default as AppMessages,
  fr: async () => (await import("@/i18n/fr.json")).default as AppMessages,
  pt: async () => (await import("@/i18n/pt.json")).default as AppMessages,
  zh: async () => (await import("@/i18n/zh.json")).default as AppMessages,
  ja: async () => (await import("@/i18n/ja.json")).default as AppMessages,
  ru: async () => (await import("@/i18n/ru.json")).default as AppMessages,
  ko: async () => (await import("@/i18n/ko.json")).default as AppMessages,
  hi: async () => (await import("@/i18n/hi.json")).default as AppMessages,
};

export function getCachedMessagesForLanguage(
  language: LanguageCode,
): AppMessages {
  return APP_MESSAGES_CACHE[language] ?? DEFAULT_MESSAGES;
}

export async function loadMessagesForLanguage(
  language: LanguageCode,
): Promise<AppMessages> {
  const cached = APP_MESSAGES_CACHE[language];
  if (cached) {
    return cached;
  }

  if (language === "es") {
    return DEFAULT_MESSAGES;
  }

  const loader = APP_MESSAGES_LOADERS[language];
  const messages = await loader();
  APP_MESSAGES_CACHE[language] = messages;
  return messages;
}
